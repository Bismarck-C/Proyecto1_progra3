    private void cargarBox(){
         ArrayList<Bloque> list = bloque.getAll();
       
       if(list != null){
           for (Bloque obj : list) {
               this.boxBloques.addItem(obj.getNombre());
               
           }
         
       }else{
            JOptionPane.showMessageDialog(this, "Error! no hay Bloque creados", "ERROR", JOptionPane.ERROR_MESSAGE);
       
       }
    
    }
