    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        String[] data = {this.txtCodigo.getText(),this.txtNewNombre.getText()};
        String[] data1 = {this.txtCodigo.getText(),this.boxEstado.getSelectedItem().toString()};
        if(!data[0].isBlank() && !data[1].isBlank()){
            bloque.cambiarEstado(data1);
            bloque.modificarBloque(data);
            JOptionPane.showMessageDialog(this, "Se modifico correctamente!");
      
        }else{
            JOptionPane.showMessageDialog(this, "No se modificaron los datos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        cleanFrame();
        
    }//GEN-LAST:event_btnAceptarMouseClicked
