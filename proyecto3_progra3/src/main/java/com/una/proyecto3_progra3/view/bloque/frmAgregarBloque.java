    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        String data[] = {this.txtCodigoBloque.getText(),this.txtNombreBloque.getText(),this.ComboBoxEstado.getSelectedItem().toString()};
        boolean bandera = true;
        if(this.txtCodigoBloque.getText().isBlank() || this.txtCodigoBloque.getText().equals("Digite el codigo del bloque") ){
            bandera = false;
        }
        if( this.txtNombreBloque.getText().isBlank() ||  this.txtNombreBloque.getText().equals("Digite el nombre del bloque") ){
            bandera = false;
        }
        
        if(bandera == true){
            if(bloque.insertBloque(data) ){
                JOptionPane.showMessageDialog(rootPane, "Se inserto correctamente");
            
             }else{
                JOptionPane.showMessageDialog(this, "Error al ingresar bloque", "ERROR", JOptionPane.ERROR_MESSAGE);
        
            }
        }else{
             JOptionPane.showMessageDialog(this, "Error! espacios en blanco", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
         cleanFrmAgregar();
    }//GEN-LAST:event_btnAceptarMouseClicked

